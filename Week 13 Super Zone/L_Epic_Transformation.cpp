void solve()
{
    int n;
    cin >> n;
    vector<int> v(n);
    map<int, int> mp;
    for (int i = 0; i < n; i++)
    {
        cin >> v[i];
        mp[v[i]]++;
    }
    int mx = INT_MIN;
    for (auto [x, y] : mp)
    {
        mx = max(mx, y);
    }
    int res = n & 1;
    if (n%2 != 0)
    {
        mx--, n--;
    }
    res += max(0, n - ((n - mx) * 2));
    cout << res << '\n';
}

